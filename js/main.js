var prac14_state = 0; // 0: have not started; 1: running; 2:paused 
var prac14_start_time = 0;  
var prac14_counter = 0;   //time elapsed in milliseconds
var prac14_timer;
var prac14_lap_counter = 1;
$("#prac14_btn1").attr("disabled", "disabled");	
function prac14_display(){
	if (prac14_counter > 6000000) {   //deal with overflow
		prac14_counter %= 6000000;
	}
	var msec = Math.floor((prac14_counter % 1000) / 10);
	var min = Math.floor(Math.floor((prac14_counter / 1000)) / 60);
	var sec = Math.floor(prac14_counter / 1000) - min * 60;
	if (msec < 10) {
		$("#prac14_msec").text( "0" + msec.toString());
	}
	else{
		$("#prac14_msec").text(msec.toString());
	}
	if (sec < 10) {
		$("#prac14_sec").text( "0" + sec.toString());
	}
	else{
		$("#prac14_sec").text(sec.toString());
	}
	if (min < 10) {
		$("#prac14_min").text( "0" + min.toString());
	}
	else{
		$("#prac14_min").text(min.toString());
	}
}
function prac14_set_timer(){
	prac14_timer = setInterval(function(){
		prac14_counter = $.now() - prac14_start_time;
		prac14_display();
	},1);
}
$("#prac14_btn1").click(function(){
	var box = '<div class="row"><div class="col-xs-6 left_align">Lap' + prac14_lap_counter + '</div><div class="col-xs-6 right_align prac14_insert"></div></div><hr>';
	if (prac14_state === 1) {    //lap
		$("#prac14_result").prepend(box);
		$("#prac14_main_display").clone().appendTo($(".prac14_insert:first"));
		prac14_lap_counter++;
	}
	else{  	           //reset
		prac14_state = 0;
		prac14_lap_counter = 1;	
		$("#prac14_btn1").attr("disabled", "disabled");
		$("#prac14_btn1").text("Lap");
		$("#prac14_btn2").text("Start");
		$("#prac14_result").text("");	
		prac14_counter = 0;
		prac14_display();
	}

});
$("#prac14_btn2").click(function(){
	if (prac14_state === 0) {     //start
		prac14_start_time = $.now();
		prac14_state = 1;
		$("#prac14_btn1").removeAttr("disabled");
		$("#prac14_btn2").text("Stop");			
		prac14_set_timer();
	}
	else if (prac14_state === 1) {   //stop
		clearInterval(prac14_timer);
		prac14_state = 2;   //paused
		$("#prac14_btn1").text("Reset");
		$("#prac14_btn2").text("Resume");
	}
	else{     //resume
		prac14_state = 1;
		prac14_start_time = $.now() - prac14_counter;
		prac14_set_timer();
		$("#prac14_btn1").text("Lap");
		$("#prac14_btn2").text("Stop");
	}
});


